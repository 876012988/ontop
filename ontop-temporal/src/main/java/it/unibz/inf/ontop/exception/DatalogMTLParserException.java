package it.unibz.inf.ontop.exception;

public class DatalogMTLParserException extends Exception {

    public DatalogMTLParserException() {
        super();
    }

    public DatalogMTLParserException(String message) {
        super(message);
    }

    public DatalogMTLParserException(String message, Throwable cause) {
        super(message, cause);
    }

    public DatalogMTLParserException(Throwable cause) {
        super(cause);
    }
}
