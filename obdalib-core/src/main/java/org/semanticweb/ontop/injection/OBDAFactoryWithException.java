package org.semanticweb.ontop.injection;

import com.google.common.collect.ImmutableList;
import org.semanticweb.ontop.exception.DuplicateMappingException;
import org.semanticweb.ontop.io.PrefixManager;
import org.semanticweb.ontop.model.OBDADataSource;
import org.semanticweb.ontop.model.OBDAMappingAxiom;
import org.semanticweb.ontop.model.OBDAModel;

import java.net.URI;
import java.util.Map;
import java.util.Set;

/**
 * Some object instantiations may throw exceptions that
 * should be expected.
 *
 * This factory is dedicated to them.
 *
 * The drawback of this approach is that
 * no implementation of this factory can be automatically
 * generated by Guice.
 */
public interface OBDAFactoryWithException {

    public OBDAModel createOBDAModel(Set<OBDADataSource> dataSources,
                                     Map<URI, ImmutableList<OBDAMappingAxiom>> newMappings,
                                     PrefixManager prefixManager) throws DuplicateMappingException;
}
